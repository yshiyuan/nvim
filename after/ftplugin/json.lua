local width = 4
vim.bo.tabstop = width -- size of a hard tabstop (ts).
vim.bo.shiftwidth = width -- size of an indentation (sw).
vim.bo.expandtab = true -- always uses spaces instead of tab characters (et).
vim.bo.softtabstop = width -- number of spaces a <Tab> counts for. When 0, feature is off (sts).
